public class Person {
	// 姓名
	private String name;
	// 年龄
	private int age;
	// 性别
	private String sex;
	// age 的 getter 方法
	public int getAge() {
		return age;
	}
	// age 的 setter 方法
	public void setAge(int age) {
		this.age = age;
	}
	// name 的 getter 方法
	public String getName() {
		return name;
	}
	// name 的 setter 方法
	public void setName(String name) {
		this.name = name;
	}
	// sex 的 getter 方法
	public String getSex() {
		return sex;
	}
	// sex 的setter 方法
	public void setSex(String sex) {
		this.sex = sex;
	}
}
